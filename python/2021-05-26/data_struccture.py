# data structure
# A data structure is a particular way of organizing data in a computer so that it can be used effectively.

# Tpes of Data Structure in python
# 1. List
# 2. Tuple
# 3. Set
# 4. Dictionary

# Data structures are used to store multiple items in a single variable.

# list ->
# ordered
# mutable
fruits = ["Apple", "Banana", "Oranges"]
print(fruits)
print(type(fruits))
print(len(fruits))

# tuple ->
# ordered
# immutable
fruits = ("Apple", "Banana", "Oranges")
print(fruits)
print(type(fruits))
print(len(fruits))

# set
# unordered
# unchangable -> cannot change its item but can remove item or add
fruits = {"Apple", "Banana", "Oranges"}
print(fruits)
print(type(fruits))
print(len(fruits))


# dictionary
# key-value pair
# mutable
myIntro = {"Name": "CMPundhir", "Age": 12}
print(myIntro)
print(type(myIntro))
print(len(myIntro))

